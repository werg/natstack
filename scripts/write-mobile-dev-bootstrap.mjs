import fs from "fs";
import path from "path";

function parseArgs(argv) {
  const args = {
    autoConnect: true,
    clear: false,
  };

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--clear") {
      args.clear = true;
    } else if (arg === "--server-url") {
      args.serverUrl = argv[++i];
    } else if (arg === "--shell-token") {
      args.shellToken = argv[++i];
    } else if (arg === "--auto-connect") {
      const value = argv[++i];
      args.autoConnect = value !== "false";
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  if (!args.clear) {
    if (!args.serverUrl) throw new Error("Missing --server-url");
    if (!args.shellToken) throw new Error("Missing --shell-token");
  }
  return args;
}

const args = parseArgs(process.argv.slice(2));
const targetPath = path.resolve("apps/mobile/src/generated/devBootstrap.ts");

const contents = args.clear ? `/**
 * Development bootstrap for the mobile shell.
 *
 * Generated by scripts/write-mobile-dev-bootstrap.mjs.
 * Regenerate this file when the local gateway URL or shell token changes.
 */

export interface DevBootstrapConfig {
  serverUrl: string;
  shellToken: string;
  autoConnect?: boolean;
}

export const devBootstrapConfig: DevBootstrapConfig | null = null;
` : `/**
 * Development bootstrap for the mobile shell.
 *
 * Generated by scripts/write-mobile-dev-bootstrap.mjs.
 * Regenerate this file when the local gateway URL or shell token changes.
 */

export interface DevBootstrapConfig {
  serverUrl: string;
  shellToken: string;
  autoConnect?: boolean;
}

export const devBootstrapConfig: DevBootstrapConfig | null = {
  serverUrl: ${JSON.stringify(args.serverUrl)},
  shellToken: ${JSON.stringify(args.shellToken)},
  autoConnect: ${args.autoConnect ? "true" : "false"},
};
`;

fs.writeFileSync(targetPath, contents, "utf8");
console.log(`Wrote ${targetPath}`);
