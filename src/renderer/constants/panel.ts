/**
 * Panel system constants
 */

/**
 * Minimum number of panel columns that can be visible at once.
 */
export const MIN_COLUMN_COUNT = 1;

/**
 * Maximum number of panel columns that can be visible at once.
 * This limit is both a UX decision (too many columns becomes cluttered)
 * and a performance consideration.
 */
export const MAX_COLUMN_COUNT = 6;

/**
 * Default number of visible panel columns on initial load.
 */
export const DEFAULT_COLUMN_COUNT = 3;
